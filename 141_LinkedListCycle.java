/**
 * Definition for singly-linked list.
 * class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
// Using two-pointer
public class Solution {
    public boolean hasCycle(ListNode head) {
        ListNode slow = head, fast = head;
        while (fast != null && fast.next != null) {
            slow = slow.next;
            fast = fast.next.next;
            if (slow == fast) {
                return true;
            }
        }
        return false;
    }
}

// Using counter
public class Solution {
    public boolean hasCycle(ListNode head) {
        long counter = 0;
        while (head != null) {
            if (counter > 10000) {
                return true;
            }
            counter++;
            head = head.next;
        }
        return false;
    }
}

